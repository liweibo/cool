package yjj.weather.net.data;

public class WeatherInData {
	private String date;
	private InfoInWeather info;
	private String week;
	private String nongli;
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public InfoInWeather getInfo() {
		return info;
	}
	public void setInfo(InfoInWeather info) {
		this.info = info;
	}
	public String getWeek() {
		return week;
	}
	public void setWeek(String week) {
		this.week = week;
	}
	public String getNongli() {
		return nongli;
	}
	public void setNongli(String nongli) {
		this.nongli = nongli;
	}
}
